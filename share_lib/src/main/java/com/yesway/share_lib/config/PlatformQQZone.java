public class PlatformQQZone {

    public String s;
    public String s1;

    public PlatformQQZone(String s, String s1) {
        this.s = s;
        this.s1 = s1;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }
}
